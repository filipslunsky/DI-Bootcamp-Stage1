# integers, i.e. 1, 2, 3, 100
# floats 32.1, 100.0

num1 = 10
num2 = 100.0

print(num1 + 30)
print(num2 + 30)

num1 = 25 # overwriting the variable/memory
print(num1)

# Mathematical operator

# addition +
5 + 5 # 10

# subtraction -
3 - 2 # 1

# multiplication *
3 * 2 # 6

# division /
5 / 5 # 1

# division without decimal, i.e. returns integer instead of float (floor) //

9 // 6 # 1

# remainder %
9 % 6 # 3

# possible to import additional math functions, e.g. "from math import ceil"

# power **
5 ** 3 # 125
    